    private void insertOrderRecord(int tableno,String bitem1,String bname1,String bQ1,String bU1,String bT1)
    {
         PreparedStatement ps;
        String query = "INSERT INTO `orders`(`TableNo`, `ItemNo`, `ItemName`, `Quantity`, `UnitPrice`,`TotalPrice`) VALUES (?,?,?,?,?,?)";
        
        try{
            ps = MyConnection.getConnection().prepareStatement(query);
            ps.setInt(1, tableno);
            ps.setString(2, bitem1);
            ps.setString(3, bname1);
            ps.setString(4, bQ1);
            ps.setString(5, bU1);
            ps.setString(6, bT1);
            if(ps.executeUpdate()>0){
                JOptionPane.showMessageDialog(null, "added");
                bq1.setText("");
            }
            Payment pk=new Payment();
            pk.setVisible(true);
            ps.close();
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog (null,e);
        }
    }
